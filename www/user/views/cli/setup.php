# You forgot to configure inKSpot

While it is totally true that inKSpot is fast and easy to use, there is
some configuration required.  You need to set up a little bit of
information so that users know where to get help, additional information,
and so that inKSpot knows how to present itself.

## Editing the inKSpot Configuration

The inKSpot configuration requires you to set at least three parameters.

	- external_domain
	- information_email
	- support_email
